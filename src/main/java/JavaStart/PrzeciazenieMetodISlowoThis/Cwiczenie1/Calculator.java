public class Calculator {
    int result;
    int add(int a, int b) {
       result = a+b;
    return result;
    }
    int add(int a, int b, int c) {
        result = a+b+c;
        return result;
    }
    int substract(int a, int b) {
        result = a-b;
        return result;
    }
    int substract(int a, int b, int c) {
        result = a-b-c;
        return result;
    }

}
