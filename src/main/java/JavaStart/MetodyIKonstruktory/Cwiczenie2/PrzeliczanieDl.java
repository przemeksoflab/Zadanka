package JavaStart.MetodyIKonstruktory.Cwiczenie2;

public class PrzeliczanieDl {

    double mForCm(double a, double b) {
        return b = a/100;
    }
    double mForMm(double a, double b) {
        return b = a/1000;
    }
    double cmForM(double a, double b) {
        return b = a*100;
    }
    double mmForM(double a, double b) {
        return b = a*1000;
    }

}
